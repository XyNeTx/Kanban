        [HttpPost]
        public IActionResult GetDateStartForecast([FromBody] string pData = null)
        {
            dynamic _json = null;
            string _SQL = "";
            try
            {
                _BearerClass.Authentication(Request);
                if (_BearerClass.Status == 401) return Content(JsonConvert.SerializeObject(_BearerClass.Result), "application/json");

                _json = JsonConvert.DeserializeObject(pData);

                _SQL = @" 
                    SELECT LEFT(StartForecastDate,4) 
	                    + '-' + SUBSTRING(StartForecastDate,5,2)
	                    + '-' + RIGHT(StartForecastDate,2) AS ForecastDate
	                    ,StartForecastDate AS StartForecastDate
                    FROM (
	                    SELECT dbo.FN_GetDateStartForecast('" + _json.StartDate + @"',3) AS StartForecastDate
	                    ) dt
                    ";
                string _nCheckDate = _KBCN.ExecuteJSON(_SQL, pUser: _BearerClass, pControllerName : ControllerContext.ActionDescriptor.ControllerName, pActionName: ControllerContext.ActionDescriptor.ActionName);

                string _result = @"{
                    ""status"":""200"",
                    ""response"":""OK"",
                    ""message"": ""Data Found"",
                    ""data"": " + _nCheckDate + @"
                }";
                return Content(_result, "application/json");
            }
            catch (Exception e)
            {
                return Content(e.Message.ToString(), "application/json");
            }
        }

    }
